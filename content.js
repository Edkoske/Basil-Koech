function populateContent() {
    Object.entries(SITE_CONTENT).forEach(([id, text]) => {
        document.querySelectorAll(`[data-template-id="${id}"]`).forEach(el => {
            el.textContent = text;
        });
    });
    Object.entries(SITE_IMAGES).forEach(([id, src]) => {
        document.querySelectorAll(`[data-template-id="${id}"]`).forEach(el => {
            if (el.tagName === 'IMG') el.src = src;
        });
    });
}
